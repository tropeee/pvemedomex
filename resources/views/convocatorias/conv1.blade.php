                  <h4>CONVOCATORIA</h4>
                  <p>A los CC. Integrantes del Consejo Político del Estado de México, electos en términos del artículo 65 de los Estatutos del Partido Verde Ecologista de México, y acorde a lo previsto por el artículo 66 fracción I y último párrafo de la referida norma interna, se les convoca a participar en la Sesión Extraordinaria del Consejo Político, que se celebrará el día 2 de septiembre de 2018 a partir de las 09:00 horas en el domicilio ubicado en República de Belice, No. 109, Colonia Américas, C.P. 50130, Toluca de Lerdo, Estado de México, de conformidad al siguiente:</p>
                  <h4>ORDEN DEL DÍA</h4>
                  <p>1.- Lista de asistencia y declaración del Quórum Legal.</p>
                  <p>2.- Discusión y en su caso aprobación del Orden del Día.</p>
                  <p>3.- Discusión y en su caso aprobación de la propuesta relacionada con la fracción XI del artículo 67 de los estatutos del Partido.</p>
                  <p>4.- Asuntos generales.</p>
                  <p>5.- Clausura y termino de la sesión.</p>
                  <h4>TRANSITORIOS</h4>
                  <p><strong>ÚNICO.-</strong>La presente Convocatoria entrará en vigor el día de su publicación y será difundida en un diario de circulación local, y en los estrados del Comité Ejecutivo del Estado de México desde el día de su publicación, y hasta el 2 de septiembre del año en curso.</p>
