                    <p>Habían pasado ya 70 años de que un mismo partido político gobernara nuestro país, cuando una modesta Organización de Colonos en los pedregales de Coyoacán fue el factor del cambio en 1979. Un día una pequeña Brigada de vecinos de una de las muchas colonias de México sufrió una terrible pérdida de sus espacios verdes y aunque a veces sea difícil de creer, este es el ejemplo claro de que con toda pérdida viene una ganancia. Fue así como surgió en ellos una intención de cambio, un espíritu de conciencia ambiental, una semilla que años más tarde sería el catalizador para el cambio pacífico del país: El Partido Verde Ecologista de México.</p>
                    <p>Este es un breve relato de la forma en la que sus integrantes buscaron la alternativa de participar políticamente, para superar los muchos obstáculos que se encontraban al tratar de salvaguardar el Medio Ambiente.</p>
                    <p>La labor constante, intensa y comprometida de la Alianza Ecologista Nacional inspiró a cientos de personas que se unieron a la causa de esta agrupación. Las ideas surgían y surgían creando así prometedores proyectos, pero esta organización carecía de los recursos necesarios para poder llevarlos a cabo.</p>
                    <p>Además, la esencia y el objetivo de la Alianza Ecologista Nacional de crear una conciencia ecológica a nivel nacional se enfrentaba también al poco interés que había en nuestra sociedad hacia las cuestiones ambientales.</p>
                    <p>Decididos a conseguir el apoyo necesario para la conservación del ambiente, esta alianza convocó a otras organizaciones con las que había colaborado para crear un partido político. Sin embargo todavía había obstáculos que enfrentar y se encontraron a la oposición de algunas organizaciones por dos principales razones</p>
                    <p>Algunos ecologistas bien intencionados pensaban que la causa ambiental, considerada como un sueño idealista, podía contaminarse por torcidas actitudes de los tradicionales partidos políticos. Otras objeciones tenían como origen intereses creados, pues a mediados de los ochentas ya existían agrupaciones ecologistas que tenían afinidad con ciertos partidos políticos.</p>
                    <p>Sin embargo, los integrantes de la Alianza Ecologista Nacional estaban decididos a convertirse en la agrupación que contagiara los ideales ambientalistas en el difícil mundo de la política y fundaron así en 1986 el Partido Verde Mexicano.</p>
                    <p>Esto no desanimó y decidieron participar en la lucha de oposición integrándose al “Frente Democrático Nacional”. Esta alianza establecía el compromiso para que el gobierno impulsara un programa ecológico. Mientras tanto el PVM se dedicaba a:</p>
                    <ul>
                        <li>Limpieza del Río Magdalena</li>
                        <li>Recolección de basura en barrancas, áreas verdes y calles de la ciudad</li>
                        <li>Denuncia de muerte masiva de aves por la contaminación en la capital.</li>
                        <li>Defensa constante de las selvas y los bosques.</li>
                        <li>Protesta contra PEMEX por el peligro y contaminación provocada por sus instalaciones.</li>
                        <li>Campañas para implantar la materia de ecología en primaria y secundaria fomentando así la educación ambiental en nuestro país.</li>
                        <li>Protestas por la destrucción de los Arrecifes de Cozumel y Cancún.</li>
                        <li>Auxilio a los damnificados por las explosiones en San Juan lxhuatepec.</li>
                        <li>Contratación de un Laboratorio Ambiental Extranjero para medir y comprobar por primera vez la alta contaminación atmosférica de la zona metropolitana de la Ciudad de México EL SOUTH WEST RESEARCH INSTITUTE.</li>
                    </ul>
                    <p>El Partido Verde Mexicano continuo trabajando en las principales causas ambientales, a la vez que solicitaba ante el Tribunal Federal Electoral la decisión sobre su registro político nacional.</p>
                    <p>El 9 de febrero de 1991 el Tribunal falló a favor de la organización, otorgándole el registro condicionado.</p>
                    <p>La Sala Central de Tribunal Federal Electoral fincó un importante precedente jurídico al resolver por unanimidad que “las labores realizadas por el Partido Verde Mexicano en defensa de la ecología, estaban orientadas a una participación activa y pública en la vida político-social del país, por tanto pueden considerarse actividades de naturaleza política”.</p>
                    <p>lnstituto Electoral, condicionó la decisión del Tribunal Federal Electoral al cambio de nombre y emblema que usaba la organización, por lo que este fue modificado y se llamó entonces Partido Verde Ecologista de México (PVEM).</p>
                    <p>La primera participación del Partido Ecologista de México en las elecciones federales de 1991 requirió de un gran esfuerzo pues todavía carecían de recursos y tenían poco apoyo por parte de los medios de comunicación. Aunque existían una enorme disparidad con los otros partidos políticos, los ecologistas lograron una votación de 1.48% dejándolos a sólo dos centésimas para poder mantener el registro.</p>
                    <p>Los verdes siguieron luchando y realizaron con mucha pasión todo el trabajo necesario para poder obtener el registro oficial. Después de 167 asambleas constitutivas y la constancia de 86 mil afiliados finalmente el 13 de enero de 1993 se recuperó el registro para poder participar en las elecciones federales de 1994.</p>
                    <p>Desde sus orígenes, el Partido Verde Ecologista de México ha luchado por convertirse en un instrumento político de representación social de las causas ecológicas de nuestro país. No cabe duda que el esfuerzo, pasión y trabajo del partido se ha visto reflejado en las urnas; esto ha sido posible gracias a la confianza depositada por los ciudadanos en la plataforma electoral del PVEM.</p>
                    <p>Hoy en dia como en 1979 nuestros ideales siguen siendo los mismos, nuestros valores se han reforzado, y nuestro deber es dar resultados a México y a los Mexicanos.</p>
